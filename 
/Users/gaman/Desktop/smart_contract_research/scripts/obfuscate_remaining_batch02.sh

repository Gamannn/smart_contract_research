
#!/bin/bash

# Obfuscate remaining batch_02 contracts with AST but no _obf.sol

ORIGINAL_DIR="/Users/gaman/Desktop/smart_contract_research/Smart-contract-obfuscation/original_contracts/batch_02"
OUTPUT_DIR="/Users/gaman/Desktop/smart_contract_research/Smart-contract-obfuscation/obfuscated_contracts/bian/batch_02"
BIAN_SRC="/Users/gaman/Desktop/smart_contract_research/BiAn/src"

mkdir -p "$OUTPUT_DIR"

cd "$BIAN_SRC" || exit 1

# List remaining: have .sol and .sol_json.ast but no _obf.sol
remaining=()
while IFS= read -r -d '' sol; do
  basename_noext=$(basename "$sol" .sol)
  if [[ -f "$ORIGINAL_DIR/${basename_noext}.sol_json.ast" && ! -f "$OUTPUT_DIR/${basename_noext}_obf.sol" ]]; then
    remaining+=("$sol")
  fi
done < <(find "$ORIGINAL_DIR" -maxdepth 1 -name "*.sol" -print0)

echo "Remaining contracts: ${#remaining[@]}"

success=0
failed=0

for sol_file in "${remaining[@]}"; do
  basename_noext=$(basename "$sol_file" .sol)
  ast_file="$ORIGINAL_DIR/${basename_noext}.sol_json.ast"
  obf_file="$OUTPUT_DIR/${basename_noext}_obf.sol"

  echo "Obfuscating $basename_noext..."

  rm -f temp.*

  cp "$sol_file" temp.sol
  cp "$ast_file" temp.json
  cp "$ast_file" temp.sol_json.ast

  python3 main.py temp.sol temp.json
  result=$?

  if [[ -f "temp_layout_confuse.sol" && -s "temp_layout_confuse.sol" ]]; then
    mv "temp_layout_confuse.sol" "$obf_file"
    echo "  ✓ Success ($(( $(stat -f%z "$obf_file") / 1000 ))KB)"
    ((success++))
  else
    echo "  ✗ Failed (code $result)"
    ((failed++))
  fi

  rm -f temp.*
done

echo "Remaining: success $success, failed $failed"

